package day_09_loop;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		int a = 5;
		//Do while loop methord
		//do{} while(condition)
		do {
		System.out.println(a);	
		a++;
		}while (a<10);
		
		
		int i = 10;
		do {
			System.out.println(i);
			i--;
		} while (i>=10);
		
		
		

	}

}
