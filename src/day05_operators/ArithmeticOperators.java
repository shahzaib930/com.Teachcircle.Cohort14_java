package day05_operators;

public class ArithmeticOperators {

	public static void main(String[] args) {
		
		// +, -, *, /, %,++, --,
		int a = 10;
		int b = 5;
		System.out.println(a+b);
		
		//Modulus % Remainder
		System.out.println(a%b);
		
		//Increment
		int valueA = 5;
		System.out.println(valueA);
		
		valueA = valueA + 1;
		System.out.println(valueA);
		
		
		//Decrement -- a = a -1
		
		valueA = 5;
		System.out.println(valueA);
		valueA = valueA - 1;
		
				
				
		
		
	}

}
